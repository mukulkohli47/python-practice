# write a program that takes two numbers and prints their sum, difference, product and whether the first number is greater than the second number or not

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

sum = a + b
difference = a - b
product = a * b
print("The sum of the two numbers is: ", sum)
print("The difference of the two numbers is: ", difference)
print("The product of the two numbers is: ", product)
print(a>b)